        public void solve(int testNumber, FastInput in, FastOutput out) {
            int cases=1;

            while(true){
                int n= in.ri ();
                int d= in.ri ();
                if(n==0) break;
                Interval[] arr=new Interval[n];
                boolean flag=true;
                for (int i = 0; i < n; i++) {
                    int x= in.ri ();
                    int y= in.ri ();
                    double beg=(double) x-Math.sqrt((double)d*d-y*y);
                    double end=(double) x+Math.sqrt((double)d*d-y*y);
                    arr[i]=new Interval (beg,end);
                    if(y>d) flag=false;
                }
                out.printf ("Case %d:",cases++);

                if(!flag) {
                    out.append (-1+"\n");
                    continue;
                }
                Arrays.sort (arr);
                double lastEnd=arr[0].end;
                int ans=0;

                for (int i = 0; i < arr.length;) {

                    double tmpEnd=lastEnd=arr[i].end;
                    for (; i <n ; i++) {
                        if(arr[i].beg>tmpEnd)
                            break;
                        tmpEnd=Math.min (tmpEnd,arr[i].end);

                    }
                    ans++;
                    lastEnd=tmpEnd;


                }
                out.append (ans+"\n");


            }


        }
